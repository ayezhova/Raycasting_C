int		ft_ispunct(int c)
{
	if (c >= '!' && c <= '/')
		return (1);
	else if (c >= 58 && c <= '?')
		return (1);
	else if (c >= 91 && c <= 96)
		return (1);
	else if (c >= 123 && c <= 126)
		return (1);
	else
		return (0);
}
